char *grammar(char text[])
{
    char text1[1000];
    int length = strlen(text);

    /* ****** FullStopper ****** */
    if (text[length - 1] != '.')
    {
        text[length] = '.';
        text[length + 1] = '\0';
    }

    for (int i = 0; i < strlen(text); i++)
    {
        /* ********* a to an ********** */
        if ((text[i] == 'a' || text[i] == 'e' || text[i] == 'i' || text[i] == 'o' || text[i] == 'u') && text[i - 1] == ' ' && text[i - 2] == 'a' && text[i - 3] == ' ')
        {
            for (int j = 0; j < i; j++)
            {
                text1[j] = text[j];
            }
            for (int j = i; j <= strlen(text); j++)
            {
                text1[j] = text[j - 1];
            }
            text1[i - 1] = 'n';
            text1[strlen(text) + 1] = '\0';
            strcpy(text, text1);
        }

        /* ********* an to a ********** */
        else if ((text[i] != 'a' && text[i] != 'e' && text[i] != 'i' && text[i] != 'o' && text[i] != 'u') && text[i - 1] == ' ' && text[i - 2] == 'n' && text[i - 3] == 'a' && text[i - 4] == ' ')
        {
            for (int j = 0; j <= (i - 3); j++)
            {
                text1[j] = text[j];
            }

            for (int j = i - 2; j < strlen(text); j++)
            {
                text1[j] = text[j + 1];
            }

            text1[strlen(text)] = '\0';
            strcpy(text, text1);
        }
    }

    return text;
}

