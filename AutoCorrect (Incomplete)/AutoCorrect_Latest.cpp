char *lower_caser(char text[])
{
    char alp[27] = {"abcdefghijklmnopqrstuvwxyz"}, ALP[27] = {"ABCDEFGHIJKLMNOPQRSTUVWXYZ"};

    for (int i = 0; i < strlen(text); i++)
    {
        for (int j = 0; j < 26; j++)
        {
            if (text[i] == ALP[j])
            {
                text[i] = alp[j];
            }
        }
    }

    return text;
}

